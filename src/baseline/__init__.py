# Baseline emotion recognition models
#
# This module contains the baseline CNN+LSTM architecture for speech emotion recognition.
# The model uses mel-spectrogram features extracted from audio waveforms.
#
# Main components:
# - model_crnn.py: CRNN (CNN + BiLSTM) architecture
# - train.py: Training script for baseline model
# - evaluate.py: Evaluation script for IEMOCAP test set
# - eval_ravdess_baseline.py: Evaluation script for RAVDESS test set
# - dataloaders.py: Data loading utilities

